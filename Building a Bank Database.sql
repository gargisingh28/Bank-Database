 ADDRESS VARCHAR(50),
 CONTACT_NO BIGINT(10),
 MAIL_ID VARCHAR(30),
 GENDER CHAR(1),
 MARITAL_STATUS VARCHAR(10),
 IDENTIFICATION_DOC_TYPE VARCHAR(20),
 ID_DOC_NO VARCHAR(20),
 CITIZENSHIP VARCHAR(10),
 CONSTRAINT CUST_PERS_INFO_PK primary key (CUSTOMER_ID)
 );

